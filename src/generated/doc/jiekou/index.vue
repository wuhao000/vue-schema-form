<h3 id="action-说明">Action 说明</h3>
<p>类型 string { name: string; text: string; props?: object; action?: ($) =&gt; {} }</p>
<p>当类型为string时，支持的值有ok，cancel，reset，分别表示提交、取消、重置操作，
当类型为object时，表示定义一个操作按钮，
text为按钮的文本，
props为按钮属性，
action为按钮点击回调,回调函数使用EffectsContext对象作为参数</p>
